	/**
	 * Request 의 데이터를 VO 배열로 변환하여 반환.
	 * @param request
	 * @return GroupOfficeListVO[]
	 */
	public GroupOfficeListVO[] fromRequestGrid(HttpServletRequest request) {
		return fromRequestGrid(request, "");
	}

	/**
	 * Request 넘어온 여러 건 DATA를 VO Class 에 담는다. 
	 * @param request
	 * @param prefix
	 * @return GroupOfficeListVO[]
	 */
	public GroupOfficeListVO[] fromRequestGrid(HttpServletRequest request, String prefix) {
		GroupOfficeListVO model = null;
		
		String[] tmp = request.getParameterValues(prefix + "ibflag");
  		if(tmp == null)
   			return null;

  		int length = request.getParameterValues(prefix + "ibflag").length;
  
		try {
			String[] ofcCd = (JSPUtil.getParameter(request, prefix	+ "ofc_cd", length));
			String[] ibflag = (JSPUtil.getParameter(request, prefix	+ "ibflag", length));
			String[] subSysCd = (JSPUtil.getParameter(request, prefix	+ "sub_sys_cd", length));
			String[] ofcGrpId = (JSPUtil.getParameter(request, prefix	+ "ofc_grp_id", length));
			String[] pagerows = (JSPUtil.getParameter(request, prefix	+ "pagerows", length));
			
			for (int i = 0; i < length; i++) {
				model = new GroupOfficeListVO();
				if (ofcCd[i] != null)
					model.setOfcCd(ofcCd[i]);
				if (ibflag[i] != null)
					model.setIbflag(ibflag[i]);
				if (subSysCd[i] != null)
					model.setSubSysCd(subSysCd[i]);
				if (ofcGrpId[i] != null)
					model.setOfcGrpId(ofcGrpId[i]);
				if (pagerows[i] != null)
					model.setPagerows(pagerows[i]);
				models.add(model);
			}

		} catch (Exception e) {
			return null;
		}
		return getGroupOfficeListVOs();
	}

	/**
	 * VO 배열을 반환
	 * @return GroupOfficeListVO[]
	 */
	public GroupOfficeListVO[] getGroupOfficeListVOs(){
		GroupOfficeListVO[] vos = (GroupOfficeListVO[])models.toArray(new GroupOfficeListVO[models.size()]);
		return vos;
	}
	
